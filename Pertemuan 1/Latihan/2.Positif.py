# Definisi dan spesifikasi dari fungsi IsPositif bernama IsPositif(x) adalah:
# IsPositif : integer --> boolean

# Realisasi
def IsPositif(x):
    return x > 0

# Aplikasi
print(IsPositif(1))
print(IsPositif(0))
print(IsPositif(-1))