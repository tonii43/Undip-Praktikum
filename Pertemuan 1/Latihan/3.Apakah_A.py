# Definisi dan spesifikasi dari fungsi IsAnA bernama IsAnA(c) adalah:
# IsAnA : character --> boolean

# Realisasi
def IsAnA(c):
    return c == 'A' or c == 'a'

# Aplikasi
print(IsAnA('A'))
print(IsAnA('a'))
print(IsAnA('B'))